public class DigitData {
    private String spelledValue;
    private int realValue;

    public DigitData(String spelledValue, int realValue) {
        this.spelledValue = spelledValue;
        this.realValue = realValue;
    }

    public String getSpelledValue() {
        return spelledValue;
    }

    public int getRealValue() {
        return realValue;
    }
}